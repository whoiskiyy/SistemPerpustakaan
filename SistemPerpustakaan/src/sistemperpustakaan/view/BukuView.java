    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            if (txtJudul.getText().isEmpty()
        || txtPenulis.getText().isEmpty()
        || txtPenerbit.getText().isEmpty()
        || txtStok.getText().isEmpty()) {

    JOptionPane.showMessageDialog(this,
            "Data Tidak Boleh Kosong");

    return;
}

    Connection conn = Koneksi.getConnection();

    String sql = "INSERT INTO buku(judul, penulis, penerbit, stok) VALUES (?, ?, ?, ?)";

    PreparedStatement pst = conn.prepareStatement(sql);

    pst.setString(1, txtJudul.getText());
    pst.setString(2, txtPenulis.getText());
    pst.setString(3, txtPenerbit.getText());
    pst.setString(4, txtStok.getText());

    pst.executeUpdate();

    JOptionPane.showMessageDialog(this,
            "Data Buku Berhasil Disimpan");
    tampilData();

} catch (Exception e) {

    JOptionPane.showMessageDialog(this,
            e.getMessage());

}        // TODO add your handling code here:
    }//GEN-LAST:event_btnSimpanActionPerformed

