    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
try {

    Connection conn = Koneksi.getConnection();

    String sql = "UPDATE buku SET judul=?, penulis=?, penerbit=?, stok=? WHERE id_buku=?";

    PreparedStatement pst = conn.prepareStatement(sql);

    pst.setString(1, txtJudul.getText());
    pst.setString(2, txtPenulis.getText());
    pst.setString(3, txtPenerbit.getText());
    pst.setString(4, txtStok.getText());
    pst.setString(5, id);

    pst.executeUpdate();

    JOptionPane.showMessageDialog(this,
            "Data Berhasil Diubah");

    tampilData();

} catch (Exception e) {

    JOptionPane.showMessageDialog(this,
            e.getMessage());

}       
    }//GEN-LAST:event_btnEditActionPerformed

